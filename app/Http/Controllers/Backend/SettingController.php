    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //print_r($request->except('_tokrn'));

        $data = new Website;

        $data->wname=$request->wname;
        $data->wtitle=$request->wtitle;
        $data->wdescription=$request->wdescription;
        $data->wemail=$request->wemail;
        $data->wphone=$request->wphone;
        $data->wfooter=$request->wfooter;
        $data->wlogo=$request->wlogo->store('image/website/');
       
        $data->save();

        return back();
    }

